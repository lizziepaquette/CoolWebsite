</p><p>
Now I can tell, (this is starting to sound pretty pretentious but bear with me) there's something there. When I don't get enough done, when I strike out with boys, when I've done something reprehensible. It's a shame, an anxiety, an anticipation of retribution, a fear. I don't want to be alone. But I fear loneliness only because I fear this feeling. Guilt. Unworthiness. When I'm alone I bandaid my feelings with affirmations and promises. I'll do better people like you you're doing fine remember this remember him.. But even though that's self reliant it doesn't deal with the problem. There's still something in me that in trying to avoid. 
</p><p>
Even this, which is making me feel better, is only good because I'm producing, and I can show it to you later and you'll approve of me and I'll feel happy. 
</p><p>
I don't feel like beating myself up over why I can't feel happy by myself. So that's good. 
</p><p>
I'm going to try not making excuses in my head anymore, or even sayin it's okay. I'm just going to feel, truly, deeply, without trepidation. And see what comes of it. Vulnerable not because I was told to be. But because I want to know what's the truth. What's deep down. What's wrong?
